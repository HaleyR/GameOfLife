def numb_to_color(number):
    if number == 0:
        return "blue"
    elif number == 1:
        return "green"
    elif number == 2:
        return "grey"
    elif number == 3:
        return "magenta"
    elif number == 4:
        return "orange"
    elif number == 5:
        return "pink"
    elif number == 6:
        return "purple"
    elif number == 7:
        return "red"
    elif number == 8:
        return "turquoise"
    elif number == 9:
        return "white"
    elif number == 10:
        return "yellow"
    else: #black
        return "black"